    "# PHASE 5: WEIGHT EXPORT, BIT-PACKING & VERIFICATION\n",
    "# ==============================================================================\n",
    "\n",
    "def pack_ternary_weights(w_quant):\n",
    "    \"\"\"Pack ternary weights {-1,0,1} into 2-bit, 4 per uint8 byte.\"\"\"\n",
    "    w_unsigned = torch.where(w_quant == -1, torch.tensor(2, dtype=torch.int8, device=w_quant.device), w_quant.to(torch.int8))\n",
    "    flat = w_unsigned.view(-1)\n",
    "    pad_len = (4 - (len(flat) % 4)) % 4\n",
    "    if pad_len > 0:\n",
    "        flat = F.pad(flat, (0, pad_len), value=0)\n",
    "    flat_reshaped = flat.view(-1, 4)\n",
    "    packed = (flat_reshaped[:, 0] | (flat_reshaped[:, 1] << 2) | (flat_reshaped[:, 2] << 4) | (flat_reshaped[:, 3] << 6))\n",
    "    return packed.to(torch.uint8)\n",
    "\n",
    "def unpack_ternary_weights(packed, original_shape):\n",
    "    \"\"\"Unpack uint8 byte array back into ternary {-1,0,1} tensor.\"\"\"\n",
