    "    flat = torch.stack([val0, val1, val2, val3], dim=1).view(-1)\n",
    "    flat_signed = torch.where(flat == 2, torch.tensor(-1, dtype=torch.int8, device=packed.device), flat.to(torch.int8))\n",
    "    num_elements = original_shape[0] * original_shape[1]\n",
    "    return flat_signed[:num_elements].view(original_shape)\n",
